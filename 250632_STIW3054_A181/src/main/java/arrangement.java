


public class arrangement 
{
    private String header;
    private String data;
     
    public arrangement(String header, String data)
    {
        this.header = header;
        this.data = data;
    }
    
    public void setHeader(String header) 
    {
        this.header = header;
    }

    public String getHeader() 
    {
        return header;
    }
    
    public void setData(String data) 
    {
        this.data = data;
    }

    public String getData() 
    {
        return data;
    }
}
